/* Includes ------------------------------------------------------------------*/
#include "main.h"

/* Private variables ---------------------------------------------------------*/
// Handlers
SPI_HandleTypeDef hspi1;
UART_HandleTypeDef huart2;

// Control variables
uint32_t blink_aux = 0;
uint32_t lora_aux = 0;
uint8_t counter = 0;

// Lora Transmission
uint8_t buffer[64];

/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART2_UART_Init(void);
static void MX_SPI1_Init(void);

int main(void)
{
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_SPI1_Init();

  // Set Lora SPI handler
  LoraSetSPIHandler(&hspi1);
  // Default Init
  if(LoraDefaultInit() != 0){
	  Error_Handler();
  }

  // Test send data
  snprintf((char *) buffer, sizeof(buffer), "Hello World");
  LoraBeginPacket(0);
  LoraTransmit((char *)buffer);
  LoraEndPacket(0);

  /* Infinite loop */
  while (1)
  {
	  // Toggle pin
	  if(HAL_GetTick() - blink_aux >= TIME_TO_BLINK){
		  HAL_GPIO_TogglePin(LED_PORT, LED_PIN);
		  blink_aux = HAL_GetTick();
	  }

	  // Transmit count number
	  if(HAL_GetTick() - lora_aux >= TIME_TO_TRANSMIT){
		  snprintf((char *) buffer, sizeof(buffer), "Count = %d", counter);
		  // Transmit
		  LoraBeginPacket(0);
		  LoraTransmit((char *)buffer);
		  LoraEndPacket(0);

		  // Bound count to 255
		  if(counter <= 254)
			  counter ++;
		  else
			  counter = 0;
		  lora_aux = HAL_GetTick();
	  }
  }
}


